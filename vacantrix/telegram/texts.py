def subscription_card(subs: list[dict],
                      hh_used: int | None,
                      avito_used: int | None,
                      display_name: str | None = None) -> str:
    lines = ["💎 <b>Моя подписка</b>"]
    if display_name:
        lines[0] += f" — {_esc(display_name)}"
    lines.append("")

    if subs:
        lines.append("✅ Активные подписки:")
        for s in subs:
            tool = (s.get("tools") or {}).get("name") or "Инструмент"
            plan = (s.get("plans") or {}).get("name") or ""
            plan_part = f" · план «{_esc(plan)}»" if plan else ""
            lines.append(f"• {_esc(tool)} — до {_fmt_date(s.get('expires_at'))}{plan_part}")
    else:
        lines.append("Активных подписок пока нет — и это не страшно: "
                     "бесплатные лимиты уже работают.")
    lines.append("")

    free_lines = []
    for label, used in (("Отклики HH", hh_used), ("Отклики Авито", avito_used)):
        if used is None:
            continue
        left = max(0, FREE_LIMIT - int(used))
        free_lines.append(f"• {label}: осталось {left} из {FREE_LIMIT}")
    if free_lines:
        lines.append("🎁 Бесплатно в этом месяце:")
        lines.extend(free_lines)
        lines.append("")

    lines.append("Оформить или продлить подписку можно в приложении "
                 "Vacantrix Platform — я потом сам принесу чек. 😉")
    return "\n".join(lines)


