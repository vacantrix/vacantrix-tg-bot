# ── Помощники ─────────────────────────────────────────────────────────────────

def _esc(value) -> str:
    return html.escape(str(value or ""), quote=False)


def _fmt_date(iso: str | None) -> str:
    """ISO-дата/датавремя → '12.08.2026' (битое значение показываем как есть)."""
    if not iso:
        return "—"
    try:
        return datetime.fromisoformat(str(iso).replace("Z", "+00:00")).strftime("%d.%m.%Y")
    except ValueError:
        return _esc(iso)


def _cap(value, limit: int) -> str:
    """Обрезка поля из БД: страховка от лимита Telegram 4096 на сообщение."""
    s = str(value or "").strip()
    return s if len(s) <= limit else s[:limit].rstrip() + "…"


