# ── Что нового (web_news) ─────────────────────────────────────────────────────

_TAG_ICON = {"release": "🚀", "update": "📦", "announce": "📣"}
_NEWS_BODY_LIMIT = 200


def news_list(rows: list[dict]) -> str:
    lines = ["🆕 <b>Что нового</b>", ""]
    shown = 0
    for n in rows:
        title = (n.get("title") or "").strip()
        if not title:
            continue
        icon = _TAG_ICON.get(n.get("tag"), "✨")
        date = _fmt_date(n.get("published_at"))
        lines.append(f"{icon} {date} — <b>{_esc(_cap(title, 120))}</b>")
        body = _cap(" ".join(str(n.get("body") or "").split()), _NEWS_BODY_LIMIT)
        if body:
            lines.append(_esc(body))
        lines.append("")
        shown += 1
    if not shown:
        return ("🆕 <b>Что нового</b>\n\n"
                "Пока тихо — но я первым расскажу, когда появятся новости!")
    lines.append("Я слежу за всеми обновлениями — важное принесу сам.")
    return "\n".join(lines)


