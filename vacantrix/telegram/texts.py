# ── Инструменты (живой каталог из tools) ──────────────────────────────────────

_STATUS_ICON = {"active": "🟢", "coming_soon": "🟡"}


def tools_catalog(rows: list[dict]) -> str:
    lines = ["🧰 <b>Инструменты Vacantrix</b>", ""]
    shown = 0
    for t in rows:
        status = t.get("status")
        icon = _STATUS_ICON.get(status)
        if not icon or not t.get("name"):
            continue
        tagline = _cap(t.get("tagline"), 96)
        tail = f" — {_esc(tagline)}" if tagline else (" — скоро!" if status == "coming_soon" else "")
        lines.append(f"{icon} <b>{_esc(_cap(t['name'], 64))}</b>{tail}")
        shown += 1
    if not shown:
        return ("🧰 <b>Инструменты Vacantrix</b>\n\n"
                "Каталог сейчас прячется от меня — загляни чуть позже "
                "или посмотри всё на сайте.")
    lines.append("")
    lines.append("🟢 — уже можно пользоваться, 🟡 — вот-вот выйдет.\n"
                 "Все приложения ставятся через Vacantrix Platform.")
    return "\n".join(lines)


